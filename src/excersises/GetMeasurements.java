package excersises;

public class GetMeasurements {
	private double centimeterMeasurement;
	private double imperialMeasurement;
	private double literMeasurement;
	
	public void setCentimeterMeasurement(double centimeter) {
		centimeterMeasurement = centimeter;
	}
	public double getCentimeterMeasurement() {
		return centimeterMeasurement;
	}
	public void setImperialMeasurement(double inch) {
		imperialMeasurement = inch;
		
	}
	public double getImperialMeasurement() {
		return imperialMeasurement;
	}
	public void setLiterMeasurement(double liter) {
		literMeasurement = liter;
	}
	public double getLiterMeasurement() {
		return literMeasurement;
	}

}
